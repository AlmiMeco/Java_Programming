package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(100 + 200); // Addition
        System.out.println(40 - 10);   // Subtraction
        System.out.println(50 / 6);    // Division
        System.out.println(7 * 3);     // Multiplication

        System.out.println("============================");

        System.out.println(10 % 3); // Modulus

        /*
        Modulus is just remainderin math:
        10/3 = 3.33333333...
        10 - (3 * 3) = 1
        Java : 10 % 3
         */
    }
}
