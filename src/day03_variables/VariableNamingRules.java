package day03_variables;

public class VariableNamingRules {
    public static void main(String[] args) {
        // age of the student is 28
        // salary is 100000
        int salary = 100000;
        System.out.println(salary);
        int age = 28;
        System.out.println(age);
        // The variable name is meaningful (age,salary) instead of something you might forget (X,A,B..)
        // Variable name MUST be unique (only used once)

        System.out.println("==============================================");
        // Zip Code is 37122
        int zipCode = 37122;
        System.out.println(zipCode);
        // The Variable name must be camelCase (zipCode) to make it more readable

        /*
        Variable Name Rules:
        1.The first charachter must be a letter (a-z ; A-Z) ... an underscore (_) ... or a dollarSign ($)
        2.The Variable Name can not include special char. or spaces
        3.Must be unique
        4.Can not be "Java Reserved Words" (check day03 slides p11)
        5.The Variable name must be camelCase, to make it more readable
        */



    }


}
